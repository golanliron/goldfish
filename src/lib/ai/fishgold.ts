// Build context string from RAG results
export function buildContext(chunks: { content: string; metadata: Record<string, unknown> }[]): string {
  if (chunks.length === 0) return '';

  const contextParts = chunks.map((chunk, i) =>
    `[מקור ${i + 1}]: ${chunk.content}`
  );

  return `\n\nהקשר מהמסמכים הארגוניים:\n${contextParts.join('\n\n')}`;
}

// Build org profile context for the AI
export function buildOrgContext(profile: Record<string, unknown> | null, orgName?: string | null, orgMemories?: { key: string; value: string }[]): string {
  const parts: string[] = ['\n\n===== כרטיס ארגון ====='];

  if (orgName) parts.push(`שם: ${orgName}`);

  if (!profile || Object.keys(profile).length === 0) {
    if (orgName) {
      parts.push('(הפרופיל עדיין ריק — צריך מסמכים או לינק כדי לבנות אותו)');
      return parts.join('\n');
    }
    parts.push('(אין פרופיל — הארגון חדש. תשאל שאלות כדי להכיר אותם)');
    return parts.join('\n');
  }

  // Build a memory lookup for overriding stale profile fields
  const mem = new Map<string, string>();
  if (orgMemories) {
    for (const m of orgMemories) mem.set(m.key, m.value);
  }

  if (profile.name && !orgName) parts.push(`שם: ${profile.name}`);
  if (mem.get('שם_עמותה')) parts.push(`שם רשום: ${mem.get('שם_עמותה')}`);
  if (mem.get('שם_מותג')) parts.push(`מותג: ${mem.get('שם_מותג')}`);
  parts.push(`ע.ר.: ${mem.get('מספר_עמותה') || profile.registration_number || ''}`);
  parts.push(`שנת הקמה: ${mem.get('שנת_הקמה') || profile.founded_year || ''}`);
  if (profile.mission) parts.push(`מטרה: ${profile.mission}`);
  if (mem.get('תקציב_2025')) {
    parts.push(`תקציב 2025: ${mem.get('תקציב_2025')}`);
  } else if (profile.annual_budget) {
    parts.push(`מחזור שנתי: ${Number(profile.annual_budget).toLocaleString('he-IL')} ש"ח`);
  }
  if (profile.employees_count) parts.push(`עובדים: ${profile.employees_count}`);
  if (profile.beneficiaries_count) parts.push(`מוטבים: ${Number(profile.beneficiaries_count).toLocaleString('he-IL')}`);
  if (profile.volunteers_count) parts.push(`מתנדבים: ${Number(profile.volunteers_count).toLocaleString('he-IL')}`);
  if (Array.isArray(profile.focus_areas)) parts.push(`תחומי פעילות: ${(profile.focus_areas as string[]).join(', ')}`);
  if (Array.isArray(profile.target_populations) && (profile.target_populations as string[]).length > 0) parts.push(`אוכלוסיות יעד: ${(profile.target_populations as string[]).join(', ')}`);
  if (mem.get('פריסה_גיאוגרפית')) {
    parts.push(`פריסה: ${mem.get('פריסה_גיאוגרפית')}`);
  } else if (Array.isArray(profile.regions)) {
    parts.push(`אזורים: ${(profile.regions as string[]).join(', ')}`);
  }
  if (Array.isArray(profile.revenue_sources) && (profile.revenue_sources as string[]).length > 0) parts.push(`מקורות הכנסה: ${(profile.revenue_sources as string[]).join(', ')}`);
  if (Array.isArray(profile.partners) && (profile.partners as string[]).length > 0) parts.push(`שותפויות: ${(profile.partners as string[]).join(', ')}`);
  if (mem.get('מנכלית') || mem.get('ceo_name')) parts.push(`מנכ"ל/ית: ${mem.get('מנכלית') || mem.get('ceo_name')}`);
  if (mem.get('מודל_פעולה') || mem.get('unique_model')) parts.push(`מודל: ${mem.get('מודל_פעולה') || mem.get('unique_model')}`);
  if (mem.get('ייחוד_מודל')) parts.push(`ייחוד: ${mem.get('ייחוד_מודל')}`);
  if (mem.get('אישורים')) parts.push(`אישורים: ${mem.get('אישורים')}`);
  if (mem.get('theory_of_change')) parts.push(`תיאוריית שינוי: ${mem.get('theory_of_change')}`);
  if (mem.get('sub_populations')) parts.push(`תתי-אוכלוסיות: ${mem.get('sub_populations')}`);
  if (mem.get('age_range')) parts.push(`טווח גילאים: ${mem.get('age_range')}`);
  if (mem.get('strength_research')) parts.push(`חוזקה מחקרית: ${mem.get('strength_research')}`);
  if (mem.get('cities_active')) parts.push(`ערים פעילות: ${mem.get('cities_active')}`);
  if (mem.get('total_beneficiaries')) parts.push(`סה"כ מוטבים: ${mem.get('total_beneficiaries')}`);

  // Show all org_memory items that aren't already displayed above
  if (orgMemories) {
    const displayedKeys = new Set(['שם_עמותה', 'שם_מותג', 'מספר_עמותה', 'שנת_הקמה', 'תקציב_2025', 'פריסה_גיאוגרפית', 'מנכלית', 'ceo_name', 'מודל_פעולה', 'unique_model', 'ייחוד_מודל', 'אישורים', 'theory_of_change', 'sub_populations', 'age_range', 'strength_research', 'cities_active', 'total_beneficiaries']);
    const extraMemories = orgMemories.filter(m => !displayedKeys.has(m.key) && m.value.length > 3);
    if (extraMemories.length > 0) {
      parts.push('\nזיכרון ארגוני נוסף:');
      for (const m of extraMemories.slice(0, 20)) {
        parts.push(`${m.key}: ${m.value}`);
      }
    }
  }

  // Impact metrics — critical for grant writing
  if (Array.isArray(profile.impact_metrics) && (profile.impact_metrics as { metric: string; value: string; year?: string }[]).length > 0) {
    parts.push('\nמדדי אימפקט:');
    for (const m of profile.impact_metrics as { metric: string; value: string; year?: string }[]) {
      parts.push(`- ${m.metric}: ${m.value}${m.year ? ` (${m.year})` : ''}`);
    }
  }

  // Contact info — critical for grant submissions
  // Try top-level fields first, then dig into nested active_projects
  let contactEmail = profile.email as string | undefined;
  let contactPhone = profile.phone as string | undefined;
  let contactWebsite = profile.website as string | undefined;
  let contactDirector = profile.ceo_name as string | undefined;

  if (Array.isArray(profile.active_projects)) {
    for (const proj of profile.active_projects as Record<string, unknown>[]) {
      if (!proj) continue;
      const org = proj.organization as Record<string, unknown> | undefined;
      const contact = org?.contact as Record<string, string> | undefined;
      if (contact) {
        if (!contactEmail && contact.email) contactEmail = contact.email;
        if (!contactPhone && contact.phone) contactPhone = contact.phone;
        if (!contactWebsite && contact.website) contactWebsite = contact.website;
        if (!contactDirector && contact.director) contactDirector = contact.director;
      }
      // Also check flat email/phone in project
      if (!contactEmail && proj.email) contactEmail = proj.email as string;
      if (!contactPhone && proj.phone) contactPhone = proj.phone as string;
      if (!contactWebsite && proj.website) contactWebsite = proj.website as string;
    }
  }

  if (contactDirector) parts.push(`מנכ"ל/ית: ${contactDirector}`);
  if (profile.contact_name) parts.push(`איש קשר: ${profile.contact_name}`);
  if (profile.contact_role) parts.push(`תפקיד: ${profile.contact_role}`);
  if (contactPhone) parts.push(`טלפון: ${contactPhone}`);
  if (contactEmail) parts.push(`מייל: ${contactEmail}`);
  if (contactWebsite) parts.push(`אתר: ${contactWebsite}`);
  if (profile.address) parts.push(`כתובת: ${profile.address}`);

  // Active projects
  if (Array.isArray(profile.active_projects) && (profile.active_projects as unknown[]).length > 0) {
    parts.push('\nפרויקטים פעילים:');
    for (const proj of profile.active_projects as { name: string; description: string; beneficiaries?: number }[]) {
      parts.push(`- ${proj.name}: ${proj.description}${proj.beneficiaries ? ` (${proj.beneficiaries} מוטבים)` : ''}`);
    }
  }

  // Key achievements
  if (Array.isArray(profile.key_achievements) && (profile.key_achievements as string[]).length > 0) {
    parts.push('\nהישגים מרכזיים:');
    for (const ach of profile.key_achievements as string[]) {
      parts.push(`- ${ach}`);
    }
  }

  // Strategic intelligence from profile (extracted by Gemini)
  if (profile.theory_of_change && !mem.get('theory_of_change')) parts.push(`\nתיאוריית שינוי: ${profile.theory_of_change}`);
  if (profile.unique_model && !mem.get('unique_model') && !mem.get('מודל_פעולה')) parts.push(`מודל ייחודי: ${profile.unique_model}`);
  if (Array.isArray(profile.strengths) && (profile.strengths as string[]).length > 0) parts.push(`חוזקות: ${(profile.strengths as string[]).join(', ')}`);
  if (Array.isArray(profile.challenges) && (profile.challenges as string[]).length > 0) parts.push(`אתגרים: ${(profile.challenges as string[]).join(', ')}`);
  if (Array.isArray(profile.sub_populations) && (profile.sub_populations as string[]).length > 0 && !mem.get('sub_populations')) parts.push(`תתי-אוכלוסיות: ${(profile.sub_populations as string[]).join(', ')}`);
  if (profile.age_range && !mem.get('age_range')) parts.push(`טווח גילאים: ${profile.age_range}`);
  if (Array.isArray(profile.certifications) && (profile.certifications as string[]).length > 0 && !mem.get('אישורים')) parts.push(`אישורים: ${(profile.certifications as string[]).join(', ')}`);

  // Summary
  if (profile.summary) parts.push(`\nסיכום: ${profile.summary}`);

  // AI insights from deep analysis — critical for grant writing
  if (profile.ai_insights) parts.push(`\nתובנות לגיוס משאבים: ${profile.ai_insights}`);
  if (Array.isArray(profile.missing_info) && (profile.missing_info as string[]).length > 0) {
    parts.push(`\nמידע חסר להשלמה: ${(profile.missing_info as string[]).join(' | ')}`);
  }

  return parts.join('\n');
}

